    private void updateExpenses() {
        try(
                Connection connection = DataBase.getConnection();
                PreparedStatement statement = connection.prepareStatement("SELECT * FROM expenses WHERE expense_id = ? AND user_id = ?");
        ){

            statement.setInt(1,SearchExpenses.id);
            statement.setInt(2,SummaryOfExpenses.idUser);
            ResultSet rs = statement.executeQuery();


            if (rs.next()) {
                int amount = rs.getInt("amount");
                String description = rs.getString("description");
                Date date = rs.getDate("expense_date");

                amountField.setText(String.valueOf(amount));
                descriptionField.setText(description);
                dateField.setText(String.valueOf(date));


            } else {
                JOptionPane.showMessageDialog(null, "No expense found with the given ID.");
                new SearchExpenses();
                dispose();
            }


        }catch (SQLException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }
    private void saveUpdateExpense(){
        try (
                Connection connection = DataBase.getConnection();
                PreparedStatement statement = connection.prepareStatement("UPDATE expenses SET amount = ? ,description = ? ,expense_date = ? WHERE expense_id = ?");

        ){

            int amount = Integer.parseInt(amountField.getText());
            String description = descriptionField.getText();
            Date date = Date.valueOf(dateField.getText());

            if (descriptionField.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Please Write your description ");
                return;
            }



            statement.setInt(1,amount);
            statement.setString(2,description);
            statement.setString(3, String.valueOf(date));
            statement.setInt(4,SearchExpenses.id);

            int rowsUpdated = statement.executeUpdate();

            if (rowsUpdated > 0){
                JOptionPane.showMessageDialog(null, "Expense updated successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "Update failed. Please check the expense ID.");
                new SearchExpenses();
                dispose();
            }
        } catch (IllegalArgumentException | SQLException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }

