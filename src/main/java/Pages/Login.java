    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
      try{

             email = emailSignIn.getText();
            String password = passwordSignIn.getText();

            if (email.isEmpty() || password.isEmpty()){
                JOptionPane.showMessageDialog(null, "Please fill out both fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (!email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
                JOptionPane.showMessageDialog(null, "Please enter a valid email address.", "Invalid Email", JOptionPane.ERROR_MESSAGE);
                return;
            }

          Connection connection = DataBase.getConnection();
          PreparedStatement preparedStatement = connection.prepareStatement("SELECT *FROM users WHERE email = ?");

            preparedStatement.setString(1,email);

            ResultSet rs = preparedStatement.executeQuery();

            if (rs.next()){

                String pass = rs.getString("password");


                    if (pass.equals(passwordSignIn.getText())){
                        SummaryOfExpenses.idUser =  rs.getInt("id");
                        new SummaryOfExpenses();
                        dispose();
                    }else {
                        JOptionPane.showMessageDialog(null, " Incorrect password ","Authentication Error",JOptionPane.WARNING_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, " Email not found . Please registrar first.","User Not Found",JOptionPane.ERROR_MESSAGE);
                    new Register();
                    dispose();
                }


       } catch (SQLException e) {
          JOptionPane.showMessageDialog(null, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
      }
    }

